fun main(args: Array<String>) {
    val students = arrayOf("John", "Alex", "Ann", "Esther", "Peter", "Jude")
    for (student in  students){
        println(student)
    }
    val browsers = arrayOf("Chrome", "Safari", "Opera", "Firefox")
    for (browser in browsers){
        println(browser)
    }
    //while loop
    var num = 5
    while (num<=10){
        println(num)
        num++
    }
    var number = 500
    while (number<=520){
        println(number)
        number++
    }



}